public class Dogs {
    private String breed;
    private int age;
    private String color;

    public Dogs(String breed, int age, String color){
        this.breed = breed;
        this.age = age;
        this.color = color;
    }

    public String getBreed() {
        return breed;
    }

    public int getAge() {
        return age;
    }

    public String getColor() {
        return color;
    }
}
